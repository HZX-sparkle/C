/*
输入一行字符，统计其中包括多少单词，单词之间用空格分隔。

输入输出格式要求：
	输入格式：a b c d回车（空格可在任意位置，列如行头行尾可能会有空格。另外数字和字母一样，也可作为单词）
	输出格式：单词数
	输入的字符串长度最长为256个字符
例如：
输入：（空格）a a 112 c（空格）回车
输出：4
*/

#include<stdio.h>
int main()
{
	char str[100], c;
	int i, num = 0, word = 0;
	gets(str);               /*输入字符串赋值给数组str*/
	for (i = 0; (c = str[i]) != '\0'; i++)
	{
		if (c == ' ')         /*如果当前字符为空格符*/
			word = 0;
		else if (word == 0) /*如果前一个字符为空格*/
		{
			word = 1;
			num++;
		}
	}
	printf("%d", num);
	return 0;
}
